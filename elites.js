function rollEliteItem(t4, blm, items, lastSlot, pity, rareRolled) {
  var roll;
  var outputItem;
  if (getRandomInt(1400 * t4 - blm) < 100) outputItem = rollEliteRareItem(eliteRareRewards, getRandomInt(eliteRareRewards.rng));
  else if (lastSlot && !rareRolled && !pity) outputItem = rollEliteRareItem(eliteSharedTable, getRandomInt(eliteSharedTable.rng));
  else outputItem = rollEliteRareItem(eliteCommonItems, getRandomInt(eliteCommonItems.rng));
  let outputAmount;
  if (outputItem.min != outputItem.max) outputAmount = getRandomInt(outputItem.max - outputItem.min + 1) + outputItem.min;
  else outputAmount = outputItem.min;
  let outputPrice = items.filter(item => item[0].toLowerCase() == outputItem.item.toLowerCase());
  roll = [{item: outputItem.item, quantity: outputAmount, price: outputPrice[0][1], total: outputPrice[0][1] * outputAmount}, outputItem.rare];
  return roll;
}

function rollEliteRareItem(item, rng) {
  for (let i of item.output) {
    if (rng <= i.roll && i.rng) {
      return rollEliteRareItem(i, getRandomInt(i.rng))
    } else if (rng <= i.roll && i.rng == null) {
      return i;
    }
  }
}

